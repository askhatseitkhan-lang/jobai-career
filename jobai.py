    def calculate_confidence_level(self, user_id, answers):
        """–†–∞—Å—á–µ—Ç —É—Ä–æ–≤–Ω—è –¥–æ—Å—Ç–æ–≤–µ—Ä–Ω–æ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤"""
